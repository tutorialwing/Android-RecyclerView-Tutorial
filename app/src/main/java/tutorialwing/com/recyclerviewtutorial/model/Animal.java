package tutorialwing.com.recyclerviewtutorial.model;

public class Animal {

	String name;
	String details;

	public String getName() {
		return name;
	}

	public void setName(String title) {
		this.name = title;
	}

	public String getDetails() {
		return details;
	}

	public void setDetails(String details) {
		this.details = details;
	}
}
